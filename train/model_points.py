import numpy as np

# Представим, что у нас есть массив с координатами точек для каждого лица
# Здесь каждая строка представляет одно лицо, а столбцы — координаты точек (x, y, z)
# Например, (x1, y1, z1, x2, y2, z2, ..., x7, y7, z7) для 7 ключевых точек
data = np.random.rand(10, 21)  # 10 лиц, 7 точек, каждая с координатами (x, y, z)

# Усреднение точек
average_points = data.mean(axis=0).reshape(-1, 3)  # Переформатирование в массив 7x3

print("Модельные точки:\n", average_points)
